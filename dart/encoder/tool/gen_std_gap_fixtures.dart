/// Generates hand-authored conformance fixtures for std base functions that
/// are genuinely unreachable from ANY source-language encoder (issue #64
/// std-coverage gap report on issue #134):
///
///   - `int_to_string` / `double_to_string` — type-specific variants of
///     `to_string`; the syntactic encoders (Dart/TS) have no static type
///     resolution (`parseString`/`ts.SyntaxKind`-only), so `.toString()`
///     always routes to the generic `to_string` and can never disambiguate
///     by receiver type.
///   - `label` (paired with `goto`, already correctly marked
///     not-encoder-emittable) — no mainstream source language the encoders
///     target has a `goto`/label-point statement (see the Dart engine's own
///     `engine: goto/label (#125)` test, which covers this the same way:
///     a hand-built `Program`, not a conformance fixture, until this tool).
///
/// Both ARE fully implemented by the Dart reference engine (and — this is
/// what makes a fixture safe to add — already compiled into the self-hosted
/// TS/C++ engines, since those are generated by encoding
/// `dart/engine/lib/engine.dart` itself, which contains this dispatch code).
/// So unlike a true engine gap, no engine work is required here — only the
/// conformance fixture was missing.
///
/// `length` is DELIBERATELY NOT generated here, unlike the other two: a
/// direct `std.length(...)` call exposed a genuine TS self-host bug — the
/// compiled engine installs a `.length` GETTER on `Object.prototype` (to
/// emulate Dart `Map.length` on every plain-object "map", including the
/// `StdModuleHandler._composedDispatch` dispatch table itself), so indexing
/// that dispatch table with the STRING KEY `"length"` returns the getter's
/// numeric result (the table's own key count) instead of `undefined`, and
/// `StdModuleHandler.call` then tries to invoke that number as a handler
/// ("composed is not a function"). This is a pre-existing, latent
/// self-hosted-engine defect (nothing to do with the encoder), never
/// exercised before because no conformance fixture called `length` until
/// this tool briefly added one. Fixing it means changing the TS engine's
/// Dart-Map-as-plain-object representation strategy — real engine work,
/// out of scope for this (encoder/conformance-surface) lane. DEFERRED:
/// re-add a `388_length_call` fixture once that representation is fixed.
///
/// Regenerate whenever these functions' semantics change:
///   cd dart/encoder && dart run tool/gen_std_gap_fixtures.dart
/// then re-run the Dart engine test suite to refresh expected_output.txt (the
/// tool computes it directly from BallEngine, matching the technique used by
/// `188_std_time_now` / the goto/label engine test).
