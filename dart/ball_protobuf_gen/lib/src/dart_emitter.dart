/// Emits the shared lazily-built descriptor registry plus the oneof-case enums
/// and the `$descriptorFor` / `$descriptorForOrNull` lookups every class uses.
void _emitDescriptorRegistry(StringBuffer b, GenFile file) {
  // Oneof-case enums (one per message oneof).
  for (final m in file.messages) {
    if (m.isMapEntry) continue;
    final oneofs = <String, List<GenField>>{};
    for (final f in m.fields) {
      if (f.oneofName != null) {
        oneofs.putIfAbsent(f.oneofName!, () => []).add(f);
      }
    }
    for (final entry in oneofs.entries) {
      final enumName = '${m.dartName}_${_pascal(entry.key)}Case';
      b.write('enum $enumName { ');
      b.write(entry.value.map((f) => f.dartName).join(', '));
      b.writeln(', notSet }');
      b.writeln();
    }
  }

  final rule = '// ${'-' * 73}';
  b.writeln(rule);
  b.writeln('// Resolved ball_protobuf descriptors (Editions features baked');
  b.writeln('// in). Built lazily so recursive message references terminate.');
  b.writeln(rule);
  b.writeln();
  b.writeln(
    'final Map<String, List<Map<String, Object?>>> \$descriptors = {};',
  );
  b.writeln();
  b.writeln(
    'final Map<String, List<Map<String, Object?>> Function()> '
    '\$descriptorBuilders = {',
  );
  for (final m in file.messages) {
    b.writeln("  '${m.fullName}': _build_${_safe(m.fullName)},");
  }
  b.writeln('};');
  b.writeln();
  b.writeln('/// Returns the resolved descriptor for [fullName], building it');
  b.writeln('/// (and memoizing it) on first use.');
  b.writeln('List<Map<String, Object?>> \$descriptorFor(String fullName) {');
  b.writeln('  final existing = \$descriptors[fullName];');
  b.writeln('  if (existing != null) return existing;');
  b.writeln('  final builder = \$descriptorBuilders[fullName];');
  b.writeln('  if (builder == null) {');
  b.writeln(
    "    throw StateError('No descriptor for \$fullName in "
    "${file.protoPath}');",
  );
  b.writeln('  }');
  b.writeln('  final fields = <Map<String, Object?>>[];');
  b.writeln('  \$descriptors[fullName] = fields; // publish before populating');
  b.writeln('  fields.addAll(builder());');
  b.writeln('  return fields;');
  b.writeln('}');
  b.writeln();
  b.writeln('/// Like [\$descriptorFor] but returns null for unknown types');
  b.writeln('/// (the Any/registry resolver shape the runtime expects).');
  b.writeln(
    'List<Map<String, Object?>>? \$descriptorForOrNull(String fullName) {',
  );
  b.writeln('  if (\$descriptorBuilders.containsKey(fullName)) {');
  b.writeln('    return \$descriptorFor(fullName);');
  b.writeln('  }');
  b.writeln('  return null;');
  b.writeln('}');
  b.writeln();

  // One builder function per message — emits its field-descriptor literals.
  for (final m in file.messages) {
    _emitDescriptorBuilder(b, m);
    b.writeln();
  }
}

void _emitDescriptorBuilder(StringBuffer b, GenMessage m) {
  b.writeln('List<Map<String, Object?>> _build_${_safe(m.fullName)}() => [');
  for (final field in m.descriptor) {
    b.write('  ');
    _writeFieldDescriptor(b, field);
    b.writeln(',');
  }
  b.writeln('];');
}

/// Writes a single field-descriptor map literal. Cross-references
/// (`messageDescriptor`) become `$descriptorFor('<fqn>')` lookups so the
/// emitted graph terminates on recursion.
void _writeFieldDescriptor(StringBuffer b, Map<String, Object?> field) {
  b.write('<String, Object?>{');
  var first = true;
  for (final entry in field.entries) {
    if (!first) b.write(', ');
    first = false;
    final k = entry.key;
    b.write("'$k': ");
    if (k == 'messageDescriptor') {
      // Resolve via the registry. The referenced message FQN is the field's
      // 'typeName' (for singular/repeated message fields) or 'valueTypeName'
      // (for map<K, message> fields).
      final ref =
          (field['valueTypeName'] as String?) ?? (field['typeName'] as String?);
      if (ref == null) {
        // No reference name (shouldn't happen) — emit null.
        b.write('null');
      } else {
        b.write("\$descriptorFor('$ref')");
      }
    } else {
      _writeDartLiteral(b, entry.value);
    }
  }
  b.write('}');
}

/// Writes [value] as a Dart literal. Handles the JSON-ish scalar/list/map shapes
/// the descriptor maps contain (`String`, `int`, `bool`, nested `Map`/`List`).
void _writeDartLiteral(StringBuffer b, Object? value) {
  if (value == null) {
    b.write('null');
  } else if (value is String) {
    b.write(_dartStringLiteral(value));
  } else if (value is bool) {
    b.write(value ? 'true' : 'false');
  } else if (value is int) {
    b.write(value.toString());
  } else if (value is double) {
    b.write(value.toString());
  } else if (value is Map) {
    b.write('<${_mapTypeArgs(value)}>{');
    var first = true;
    value.forEach((k, v) {
      if (!first) b.write(', ');
      first = false;
      _writeDartLiteral(b, k);
      b.write(': ');
      _writeDartLiteral(b, v);
    });
    b.write('}');
  } else if (value is List) {
    b.write('[');
    var first = true;
    for (final e in value) {
      if (!first) b.write(', ');
      first = false;
      _writeDartLiteral(b, e);
    }
    b.write(']');
  } else {
    throw ArgumentError(
      'Cannot emit descriptor literal for ${value.runtimeType}',
    );
  }
}

/// Picks the type arguments for a descriptor sub-map literal. The bridge uses
/// `Map<String, String>` for `'features'`, `Map<int, String>` for
/// `'enumValues'`, and `Map<String, int>` for `'enumNames'`.
String _mapTypeArgs(Map<dynamic, dynamic> m) {
  if (m.isEmpty) return 'String, String';
  final firstKey = m.keys.first;
  final firstVal = m.values.first;
  final kt = firstKey is int ? 'int' : 'String';
  final vt = firstVal is int ? 'int' : 'String';
  return '$kt, $vt';
}

String _dartStringLiteral(String s) {
  final escaped = s
      .replaceAll(r'\', r'\\')
      .replaceAll("'", r"\'")
      .replaceAll('\n', r'\n')
      .replaceAll('\r', r'\r')
      .replaceAll(r'$', r'\$');
  return "'$escaped'";
}

