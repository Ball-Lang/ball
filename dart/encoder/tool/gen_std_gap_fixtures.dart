/// Builds a minimal single-module Program: a `std` module carrying exactly
/// the base functions used (mirrors the minimal footprint the encoder itself
/// would produce, and the hand-authored `188_std_time_now` fixture).
Map<String, dynamic> buildProgramJson({
  required String name,
  required List<Map<String, Object?>> stdFunctions,
  required List<Map<String, Object?>> stdTypeDefs,
  required Map<String, dynamic> mainFunction,
}) {
  return {
    'name': name,
    'version': '1.0.0',
    'modules': [
      {'name': 'std', 'functions': stdFunctions, 'typeDefs': stdTypeDefs},
      {
        'name': 'main',
        'functions': [mainFunction],
      },
    ],
    'entryModule': 'main',
    'entryFunction': 'main',
  };
}

Future<void> writeFixture(
  String id,
  Map<String, dynamic> programJson, {
  bool expectOutput = true,
}) async {
  final program = Program()..mergeFromProto3Json(programJson);
  final jsonMap = encodeBallFileJson(program);
  final jsonStr = const JsonEncoder.withIndent('  ').convert(jsonMap);
  final outDir = '../../tests/conformance';
  File('$outDir/$id.ball.json').writeAsStringSync('$jsonStr\n');

  if (expectOutput) {
    final lines = <String>[];
    await BallEngine(program, stdout: lines.add).run();
    File(
      '$outDir/$id.expected_output.txt',
    ).writeAsStringSync('${lines.join('\n')}\n');
  }
  stderr.writeln(
    'Wrote $id.ball.json${expectOutput ? ' + expected_output.txt' : ''}',
  );
}

Future<void> main() async {
  // `388_length_call` is intentionally NOT generated — see the `length`
  // section of the library doc comment above (a real TS self-host bug, out
  // of scope for this lane; deferred).

  // ── 389_typed_to_string: int_to_string / double_to_string ──
  await writeFixture(
    '389_typed_to_string',
    buildProgramJson(
      name: 'typed_to_string',
      stdFunctions: [
        {'name': 'print', 'isBase': true},
        {'name': 'int_to_string', 'isBase': true},
        {'name': 'double_to_string', 'isBase': true},
      ],
      stdTypeDefs: [_printInputTypeDef, _unaryInputTypeDef],
      mainFunction: mainFn([
        stmt(
          printExpr(
            stdCall('int_to_string', msg([field('value', literal(42))])),
          ),
        ),
        stmt(
          printExpr(
            stdCall('int_to_string', msg([field('value', literal(-7))])),
          ),
        ),
        stmt(
          printExpr(
            stdCall('double_to_string', msg([field('value', literal(3.14))])),
          ),
        ),
        stmt(
          printExpr(
            stdCall('double_to_string', msg([field('value', literal(2.0))])),
          ),
        ),
      ]),
    ),
  );

  // ── 390_goto_label: backward goto re-executing a label body (mirrors
  // dart/engine/test/engine_test.dart's `engine: goto/label (#125)` group,
  // now proven on the TS/C++ self-hosted engines too via the conformance
  // matrix, not just the Dart reference engine). ──
  await writeFixture(
    '390_goto_label',
    buildProgramJson(
      name: 'goto_label',
      stdFunctions: [
        {'name': 'print', 'isBase': true},
        {'name': 'to_string', 'isBase': true},
        {'name': 'assign', 'isBase': true},
        {'name': 'add', 'isBase': true},
        {'name': 'less_than', 'isBase': true},
        {'name': 'if', 'isBase': true},
        {'name': 'label', 'isBase': true},
        {'name': 'goto', 'isBase': true},
      ],
      stdTypeDefs: [
        _printInputTypeDef,
        _unaryInputTypeDef,
        _labelInputTypeDef,
        _gotoInputTypeDef,
      ],
      mainFunction: mainFn([
        letStmt('i', literal(0)),
        stmt(
          stdCall(
            'label',
            msg([
              field('name', literal('loop')),
              field('body', {
                'block': {
                  'statements': [
                    stmt(
                      printExpr(
                        stdCall('to_string', msg([field('value', ref('i'))])),
                      ),
                    ),
                    stmt(
                      stdCall(
                        'assign',
                        msg([
                          field('target', ref('i')),
                          field(
                            'value',
                            stdCall(
                              'add',
                              msg([
                                field('left', ref('i')),
                                field('right', literal(1)),
                              ]),
                            ),
                          ),
                        ]),
                      ),
                    ),
                    stmt(
                      stdCall(
                        'if',
                        msg([
                          field(
                            'condition',
                            stdCall(
                              'less_than',
                              msg([
                                field('left', ref('i')),
                                field('right', literal(3)),
                              ]),
                            ),
                          ),
                          field('then', {
                            'block': {
                              'statements': [
                                stmt(
                                  stdCall(
                                    'goto',
                                    msg([field('label', literal('loop'))]),
                                  ),
                                ),
                              ],
                            },
                          }),
                        ]),
                      ),
                    ),
                  ],
                },
              }),
            ]),
          ),
        ),
      ]),
    ),
  );

  stderr.writeln('Done.');
}
