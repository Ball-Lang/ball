"""Regenerate ``compiled_engine.py`` from the self-hosted Ball engine.

The self-host route (SKILL.md Phase 4 Option B), mirroring
``go/engine/cmd/regen`` / ``rust/engine/tool`` / ``csharp/engine/tool``: read the
reference engine's Ball encoding (``dart/self_host/engine.ball.json``, a
gitignored artifact from ``dart run compiler/tool/gen_engine_json.dart``), compile
it through ``ball_compiler`` in **library mode** (the engine is a multi-module
Program of classes, not a runnable script), and write the module with a
NEVER-EDIT header.

``compiled_engine.py`` is a GENERATED, gitignored build artifact — to change it,
fix ``python/compiler`` or ``python/runtime`` and rerun this.

Usage:  python -m ball_engine.regen
"""

from __future__ import annotations

import sys
from pathlib import Path

_HERE = Path(__file__).resolve()


def _repo_root() -> Path:
    d = _HERE.parent
    while d != d.parent:
        if (d / "proto" / "ball" / "v1" / "ball.proto").exists():
            return d
        d = d.parent
    raise SystemExit("[regen] not inside the ball repo (no proto/ball/v1/ball.proto)")


HEADER = (
    "# Code generated by `python -m ball_engine.regen` from "
    "dart/self_host/engine.ball.json.\n"
    "# DO NOT EDIT BY HAND. This is the self-hosted Ball engine, compiled from the\n"
    "# reference engine's own Ball encoding through the Ball -> Python compiler. To\n"
    "# change it, fix python/compiler or python/runtime (or the dart/self_host/\n"
    "# source) and rerun the regenerator. See python/engine/AGENTS.md.\n\n"
)


def main() -> int:
    root = _repo_root()
    # Import the sibling compiler + runtime by path (the packages are isolated).
    sys.path.insert(0, str(root / "python" / "compiler"))
    sys.path.insert(0, str(root / "python" / "runtime"))
    from ball_compiler.loader import load_program
    from ball_compiler.compiler import compile_library

    json_path = root / "dart" / "self_host" / "engine.ball.json"
    if not json_path.exists():
        raise SystemExit(
            f"[regen] {json_path} is missing; regenerate the self-host source first:\n"
            f"          cd dart && dart run compiler/tool/gen_engine_json.dart"
        )

    print(f"[regen] loading {json_path}", file=sys.stderr)
    program = load_program(json_path)
    modules = program.get("modules", [])
    print(f"[regen] compiling program {program.get('name', '')!r} "
          f"v{program.get('version', '')} ({len(modules)} modules)", file=sys.stderr)

    src = compile_library(program)
    out_path = _HERE.parent / "compiled_engine.py"
    out_path.write_text(HEADER + src, encoding="utf-8")
    print(f"[regen] wrote {out_path} ({len(HEADER) + len(src)} bytes)", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
