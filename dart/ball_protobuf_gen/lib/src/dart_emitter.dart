void _emitMessage(StringBuffer b, GenMessage m) {
  b.writeln('/// Generated from `${m.fullName}`.');
  b.writeln('class ${m.dartName} {');
  b.writeln('  /// The dynamic backing store (proto field names -> values).');
  b.writeln('  final Map<String, Object?> \$fields;');
  b.writeln();
  b.writeln(
    '  ${m.dartName}([Map<String, Object?>? fields]) '
    ': \$fields = fields ?? <String, Object?>{};',
  );
  b.writeln();
  b.writeln('  /// The fully-qualified protobuf type name.');
  b.writeln("  static const String fullName = '${m.fullName}';");
  b.writeln();
  b.writeln(
    '  /// The resolved ball_protobuf field descriptor (Editions-aware).',
  );
  b.writeln(
    '  static List<Map<String, Object?>> get descriptor => '
    "\$descriptorFor('${m.fullName}');",
  );
  b.writeln();

  // Oneof discriminants.
  final oneofs = <String, List<GenField>>{};
  for (final f in m.fields) {
    if (f.oneofName != null) {
      oneofs.putIfAbsent(f.oneofName!, () => []).add(f);
    }
  }
  for (final entry in oneofs.entries) {
    _emitOneofDiscriminant(b, m, entry.key, entry.value);
  }

  // Field accessors.
  for (final f in m.fields) {
    _emitField(b, m, f);
  }

  // Serialization delegates.
  b.writeln('  /// Encodes this message to protobuf binary bytes.');
  b.writeln('  List<int> toBytes() => \$pb.marshal(\$fields, descriptor);');
  b.writeln();
  b.writeln('  /// Decodes [bytes] into a new ${m.dartName}.');
  b.writeln(
    '  static ${m.dartName} fromBytes(List<int> bytes) => '
    '${m.dartName}(\$pb.unmarshal(bytes, descriptor));',
  );
  b.writeln();
  b.writeln('  /// Converts this message to its proto3-JSON representation.');
  b.writeln(
    '  Object? toProto3Json() => \$pb.messageToJson(\$fields, descriptor,\n'
    '      anyTypeResolver: \$descriptorForOrNull);',
  );
  b.writeln();
  b.writeln(
    '  /// Parses a proto3-JSON value [json] into a new ${m.dartName}.',
  );
  b.writeln(
    '  static ${m.dartName} fromProto3Json(Object? json) => ${m.dartName}(\n'
    '      \$pb.messageFromJson(json, descriptor,\n'
    '          anyTypeResolver: \$descriptorForOrNull));',
  );
  b.writeln('}');
}

void _emitOneofDiscriminant(
  StringBuffer b,
  GenMessage m,
  String oneofName,
  List<GenField> members,
) {
  final enumName = '${m.dartName}_${_pascal(oneofName)}Case';
  b.writeln('  /// Discriminant for the `$oneofName` oneof.');
  b.writeln('  $enumName get which${_pascal(oneofName)} {');
  for (final f in members) {
    b.writeln(
      "    if (\$fields['${f.protoName}'] != null) {\n"
      '      return $enumName.${f.dartName};\n'
      '    }',
    );
  }
  b.writeln('    return $enumName.notSet;');
  b.writeln('  }');
  b.writeln();
  b.writeln('  /// Clears whichever member of `$oneofName` is set.');
  b.writeln('  void clear${_pascal(oneofName)}() {');
  for (final f in members) {
    b.writeln("    \$fields.remove('${f.protoName}');");
  }
  b.writeln('  }');
  b.writeln();
}

void _emitField(StringBuffer b, GenMessage m, GenField f) {
  switch (f.cardinality) {
    case FieldCardinality.singular:
      _emitSingular(b, m, f);
    case FieldCardinality.repeated:
      _emitRepeated(b, f);
    case FieldCardinality.map:
      _emitMap(b, f);
  }
}

void _emitSingular(StringBuffer b, GenMessage m, GenField f) {
  final key = f.protoName;
  if (f.isMessage) {
    final type = _messageDartType(f.messageTypeName!);
    // Getter returns a typed view over the stored sub-map (or null when absent).
    b.writeln('  /// `${f.protoName}` (message `${f.messageTypeName}`).');
    b.writeln('  $type? get ${f.dartName} {');
    b.writeln("    final v = \$fields['$key'];");
    b.writeln('    if (v == null) return null;');
    b.writeln('    return $type(v as Map<String, Object?>);');
    b.writeln('  }');
    b.writeln(
      '  set ${f.dartName}($type? v) => v == null\n'
      "      ? \$fields.remove('$key')\n"
      "      : \$fields['$key'] = v.\$fields;",
    );
    _emitPresence(b, f, key);
    return;
  }

  if (f.isEnum) {
    final type = _enumDartType(f.enumTypeName!);
    b.writeln('  /// `${f.protoName}` (enum `${f.enumTypeName}`).');
    if (f.presence == PresenceKind.implicit) {
      b.writeln('  $type get ${f.dartName} {');
      b.writeln("    final v = \$fields['$key'];");
      b.writeln('    return $type.fromValue(v == null ? 0 : v as int);');
      b.writeln('  }');
      b.writeln("  set ${f.dartName}($type v) => \$fields['$key'] = v.value;");
    } else {
      b.writeln('  $type? get ${f.dartName} {');
      b.writeln("    final v = \$fields['$key'];");
      b.writeln('    return v == null ? null : $type.fromValue(v as int);');
      b.writeln('  }');
      b.writeln(
        '  set ${f.dartName}($type? v) => v == null\n'
        "      ? \$fields.remove('$key')\n"
        "      : \$fields['$key'] = v.value;",
      );
    }
    _emitPresence(b, f, key);
    return;
  }

  // Scalar. The getter always falls back to the type default; EXPLICIT /
  // LEGACY_REQUIRED presence is distinguished only by the generated
  // `hasX()` / `clearX()` (and by the descriptor, which makes the runtime
  // serialize a present default). IMPLICIT fields get no presence accessors.
  final type = _scalarDartType(f.protoType);
  final defaultExpr = _scalarDefault(f.protoType);
  b.writeln('  /// `${f.protoName}`.');
  b.writeln(
    '  $type get ${f.dartName} => '
    "(\$fields['$key'] as $type?) ?? $defaultExpr;",
  );
  b.writeln("  set ${f.dartName}($type v) => \$fields['$key'] = v;");
  _emitPresence(b, f, key);
}

/// For EXPLICIT / LEGACY_REQUIRED fields, emit `hasX()` / `clearX()`. IMPLICIT
/// fields get neither (presence is value != default, with no tracking).
void _emitPresence(StringBuffer b, GenField f, String key) {
  if (f.presence == PresenceKind.implicit) {
    b.writeln();
    return;
  }
  final name = _pascal(f.dartName);
  b.writeln("  bool has$name() => \$fields['$key'] != null;");
  b.writeln("  void clear$name() => \$fields.remove('$key');");
  b.writeln();
}

void _emitRepeated(StringBuffer b, GenField f) {
  final key = f.protoName;
  final String elemType;
  if (f.isMessage) {
    elemType = _messageDartType(f.messageTypeName!);
  } else if (f.isEnum) {
    elemType = _enumDartType(f.enumTypeName!);
  } else {
    elemType = _scalarDartType(f.protoType);
  }
  b.writeln('  /// repeated `${f.protoName}`.');
  if (f.isMessage) {
    // Typed view over the stored list of sub-maps.
    b.writeln('  List<$elemType> get ${f.dartName} {');
    b.writeln("    final v = \$fields['$key'] as List?;");
    b.writeln('    if (v == null) return <$elemType>[];');
    b.writeln(
      '    return [for (final e in v) '
      '$elemType(e as Map<String, Object?>)];',
    );
    b.writeln('  }');
    b.writeln(
      '  set ${f.dartName}(List<$elemType> v) =>\n'
      "      \$fields['$key'] = [for (final e in v) e.\$fields];",
    );
  } else if (f.isEnum) {
    b.writeln('  List<$elemType> get ${f.dartName} {');
    b.writeln("    final v = \$fields['$key'] as List?;");
    b.writeln('    if (v == null) return <$elemType>[];');
    b.writeln('    return [for (final e in v) $elemType.fromValue(e as int)];');
    b.writeln('  }');
    b.writeln(
      '  set ${f.dartName}(List<$elemType> v) =>\n'
      "      \$fields['$key'] = [for (final e in v) e.value];",
    );
  } else {
    b.writeln(
      '  List<$elemType> get ${f.dartName} =>\n'
      "      (\$fields['$key'] as List?)?.cast<$elemType>() ?? <$elemType>[];",
    );
    b.writeln(
      "  set ${f.dartName}(List<$elemType> v) => \$fields['$key'] = v;",
    );
  }
  b.writeln();
}

void _emitMap(StringBuffer b, GenField f) {
  final key = f.protoName;
  final keyType = _scalarDartType(f.mapKeyType!);
  final String valType;
  if (f.mapValueType == 'TYPE_MESSAGE') {
    valType = _messageDartType(f.messageTypeName!);
  } else if (f.mapValueType == 'TYPE_ENUM') {
    valType = _enumDartType(f.enumTypeName!);
  } else {
    valType = _scalarDartType(f.mapValueType!);
  }
  b.writeln('  /// map `${f.protoName}`.');
  if (f.mapValueType == 'TYPE_MESSAGE') {
    b.writeln('  Map<$keyType, $valType> get ${f.dartName} {');
    b.writeln("    final v = \$fields['$key'] as Map?;");
    b.writeln('    if (v == null) return <$keyType, $valType>{};');
    b.writeln('    return {');
    b.writeln(
      '      for (final e in v.entries)\n'
      '        e.key as $keyType: '
      '$valType(e.value as Map<String, Object?>),',
    );
    b.writeln('    };');
    b.writeln('  }');
    b.writeln('  set ${f.dartName}(Map<$keyType, $valType> v) {');
    b.writeln('    \$fields[\'$key\'] = {');
    b.writeln('      for (final e in v.entries) e.key: e.value.\$fields,');
    b.writeln('    };');
    b.writeln('  }');
  } else if (f.mapValueType == 'TYPE_ENUM') {
    b.writeln('  Map<$keyType, $valType> get ${f.dartName} {');
    b.writeln("    final v = \$fields['$key'] as Map?;");
    b.writeln('    if (v == null) return <$keyType, $valType>{};');
    b.writeln('    return {');
    b.writeln(
      '      for (final e in v.entries)\n'
      '        e.key as $keyType: $valType.fromValue(e.value as int),',
    );
    b.writeln('    };');
    b.writeln('  }');
    b.writeln('  set ${f.dartName}(Map<$keyType, $valType> v) {');
    b.writeln('    \$fields[\'$key\'] = {');
    b.writeln('      for (final e in v.entries) e.key: e.value.value,');
    b.writeln('    };');
    b.writeln('  }');
  } else {
    b.writeln(
      '  Map<$keyType, $valType> get ${f.dartName} =>\n'
      "      (\$fields['$key'] as Map?)?.cast<$keyType, $valType>() ??\n"
      '      <$keyType, $valType>{};',
    );
    b.writeln(
      "  set ${f.dartName}(Map<$keyType, $valType> v) => \$fields['$key'] = v;",
    );
  }
  b.writeln();
}

