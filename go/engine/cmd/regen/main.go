// Command regen regenerates go/engine/compiled/compiled_engine.go from the
// self-hosted Ball engine (dart/self_host/engine.ball.pb) — the self-host route
// (epic #426 Phase 4, SKILL.md Phase 4 Option B), mirroring
// rust/engine/tool/src/main.rs and csharp/engine/tool/Program.cs.
//
// Pipeline: read engine.ball.pb (a google.protobuf.Any envelope wrapping the
// ball.v1.Program; falls back to engine.ball.json, stripping the @type
// envelope), unpack to a typed Program, compile via compiler.CompileLibrary in
// package `compiled` (library shape — the engine has no runnable top-level
// entry point; its public surface is the BallEngine/StdModuleHandler classes the
// wrapper constructs), gofmt, and write with a NEVER-edit header.
//
// compiled_engine.go is a GENERATED, NEVER-edit, gitignored build artifact — fix
// the compiler (go/compiler) or the wrapper and rerun this.
package main

import (
	"fmt"
	"os"
	"path/filepath"

	compiler "github.com/ball-lang/ball/go/compiler"
	ballv1 "github.com/ball-lang/ball/go/shared/gen/ball/v1"
	"google.golang.org/protobuf/proto"
	"google.golang.org/protobuf/types/known/anypb"
)

const header = `// Code generated by ` + "`go run ./cmd/regen`" + ` from dart/self_host/engine.ball.pb.
//
// DO NOT EDIT BY HAND. This is the self-hosted Ball engine, compiled from the
// reference engine's own Ball encoding through the Ball -> Go compiler. To change
// it, fix go/compiler (or the dart/self_host/ source) and rerun the regenerator.
// See go/engine/AGENTS.md. Only participates in the build under the selfhost
// build tag.

`

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, "[regen]", err)
		os.Exit(1)
	}
}

func run() error {
	root, err := findRepoRoot()
	if err != nil {
		return err
	}
	pbPath := filepath.Join(root, "dart", "self_host", "engine.ball.pb")
	jsonPath := filepath.Join(root, "dart", "self_host", "engine.ball.json")
	outPath := filepath.Join(root, "go", "engine", "compiled", "compiled_engine.go")

	program, err := loadProgram(pbPath, jsonPath)
	if err != nil {
		return err
	}
	fmt.Fprintf(os.Stderr, "[regen] compiling program %q v%s (%d modules)\n",
		program.GetName(), program.GetVersion(), len(program.GetModules()))

	src, compileErr := compiler.CompileLibrary(program, "compiled")

	// Try to gofmt; if the emitted Go is not yet valid (a compiler gap), write
	// the raw source so it can be inspected, and surface the error.
	out := src
	if formatted, ferr := compiler.Format(src); ferr == nil {
		out = formatted
	} else {
		fmt.Fprintf(os.Stderr, "[regen] gofmt failed (emitted Go not yet valid): %v\n", ferr)
	}

	if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
		return err
	}
	if err := os.WriteFile(outPath, []byte(header+out), 0o644); err != nil {
		return err
	}
	fmt.Fprintf(os.Stderr, "[regen] wrote %s (%d bytes)\n", outPath, len(header)+len(out))

	if compileErr != nil {
		return fmt.Errorf("compile reported unsupported constructs:\n%w", compileErr)
	}
	return nil
}

func loadProgram(pbPath, jsonPath string) (*ballv1.Program, error) {
	if data, err := os.ReadFile(pbPath); err == nil {
		fmt.Fprintf(os.Stderr, "[regen] loading %s\n", pbPath)
		var any anypb.Any
		if err := proto.Unmarshal(data, &any); err == nil {
			var program ballv1.Program
			if err := any.UnmarshalTo(&program); err == nil {
				return &program, nil
			}
		}
		// A bare (non-Any) Program binary.
		return compiler.LoadProgramBinary(data)
	}
	data, err := os.ReadFile(jsonPath)
	if err != nil {
		return nil, fmt.Errorf("neither %s nor %s is readable; regenerate the self-host source first "+
			"(dart run dart/compiler/tool/gen_engine_json.dart): %w", pbPath, jsonPath, err)
	}
	fmt.Fprintf(os.Stderr, "[regen] loading %s\n", jsonPath)
	return compiler.LoadProgramJSON(data)
}

func findRepoRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "proto", "ball", "v1", "ball.proto")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("not inside the ball repo (no proto/ball/v1/ball.proto found)")
		}
		dir = parent
	}
}
