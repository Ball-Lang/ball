//! `ball-engine` â€” runs Ball programs via the self-hosted engine (issue #39).
//!
//! ## Design: self-host, mirroring the TS/C++ targets
//!
//! Rather than hand-writing a tree-walking interpreter, `ball-engine` follows
//! the recommended self-hosting route (see
//! `.claude/skills/new-ball-language/SKILL.md` Phase 4, Option B): the
//! reference engine is itself a Ball program
//! (`dart/self_host/engine.ball.json`), which `ball-engine-regen`
//! (`rust/engine/tool/`) compiles through `ball-compiler` into
//! `src/compiled_engine.rs`. A thin wrapper (this crate) then supplies what
//! compiled Ball can't express natively:
//! - loading a target [`Program`] from proto3-JSON `.ball.json` or binary
//!   `.ball.bin` and viewing it as the canonical proto3-JSON [`BallValue`] the
//!   compiled engine reads ([`loader`]);
//! - the `ball_proto` access-pattern functions the compiled engine calls to
//!   inspect that program â€” oneof discriminators, presence checks, safe field
//!   access ([`ball_proto`]);
//! - the lexical [`scope`] chain and [`scope::FlowSignal`] flow signals;
//! - virtual properties ([`ball_proto::virtual_property`]).
//!
//! `src/compiled_engine.rs` is a **generated, NEVER-edit** file â€” regenerate
//! it with `cargo run -p ball-engine-regen`, never hand-patch it.
//!
//! ## Self-host status (#39/#298/#300)
//!
//! The self-hosted engine compiles cleanly **and RUNS** (`cargo build -p
//! ball-engine --features self_host`; the `self_host_run` integration tests).
//! Issue #300 wired the base-function runtime surface â€” `ball_proto.*`
//! AST-inspection, `std.map_create`/`record`/`typed_list`/`switch_expr`/â€¦,
//! `std_collections`/`std_convert`, and the class-hierarchy/wrapper-class
//! plumbing â€” so [`BallEngine::run`] (under `self_host`) constructs the compiled
//! `main_BallEngine`, registers the `StdModuleHandler`, drives its `run`, and
//! produces output **identical to the Dart reference engine**: `hello_world`
//! prints `Hello, World!` and recursive `fibonacci(10)` prints `55`. The key
//! enablers beyond the base-function dispatch were reference-`this` (#298),
//! control-flow-through-`try` (`return`/`break`/`continue` escape a `try`'s
//! `catch_unwind` closure via `ball_shared::runtime::BallFlow`), the
//! method/constructor field write-back-on-early-`return` IIFE, and a large-stack
//! run thread. The default build (no `self_host`) still returns
//! [`EngineError::SelfHostPending`].
//!
//! **Corpus status (#39/#300): see `rust/engine/AGENTS.md` for the current
//! tally and per-bucket categorization** (this comment does not track it). The
//! loop-scope, string-surface, and reference-semantic-`List` blockers are
//! **resolved**:
//! - Loop counters now persist. The engine routes `i++`/`i--` through a Dart
//!   `switch` whose increment/decrement labels *fall through* to one shared
//!   body; the Rust compiler lowered `switch` to an if-chain that gave each
//!   body-less fall-through label an empty arm, so three of the four
//!   increment/decrement functions silently no-op'd and every `for`/`while`
//!   loop spun forever. `compile_switch` now implements fall-through, so
//!   loops/recursion-with-loops/closures run (`self_host_run` acceptance tests).
//! - String/field access works. The engine reads `FieldAccess.field` via the
//!   Dart-renamed getter `.field_2`; `ball_field_get` now aliases that (and
//!   `descriptor_`) back to the proto key, and the loader materializes proto3
//!   defaults (`skip_default_fields(false)`) so an empty list literal `[]` no
//!   longer reads back as `null`.
//!
//! **Reference-semantic `List` (issues #39/#300) â€” DONE.** Dart's `List` is a
//! reference type: the engine mutates a list passed as a function argument
//! (`_addCollectionElement(element, scope, result)` appends to `result`), and
//! `BallValue::List` now shares its backing (`Arc<Mutex<Vec>>`, like
//! `BallMessage`), so that append is observed by the caller and list literals /
//! comprehensions / sorts evaluate correctly. Only aliasing *reads* share; the
//! copy points (`toList()`/`List.from`/spread) still snapshot. See
//! `rust/shared/src/value.rs` (`BallList`) and `rust/engine/AGENTS.md`.
//!
//! **Remaining (follow-up):** some class `toString`/constructor edge cases. The
//! default build (no `self_host`) still returns `EngineError::SelfHostPending`;
//! its foundation (loader + scope + ball_proto) stays green.

