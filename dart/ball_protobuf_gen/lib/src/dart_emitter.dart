/// The Dart type for a singular scalar protobuf [type].
String _scalarDartType(String type) {
  switch (type) {
    case 'TYPE_INT32':
    case 'TYPE_INT64':
    case 'TYPE_UINT32':
    case 'TYPE_UINT64':
    case 'TYPE_SINT32':
    case 'TYPE_SINT64':
    case 'TYPE_FIXED32':
    case 'TYPE_FIXED64':
    case 'TYPE_SFIXED32':
    case 'TYPE_SFIXED64':
      return 'int';
    case 'TYPE_FLOAT':
    case 'TYPE_DOUBLE':
      return 'double';
    case 'TYPE_BOOL':
      return 'bool';
    case 'TYPE_STRING':
      return 'String';
    case 'TYPE_BYTES':
      return 'List<int>';
    default:
      throw ArgumentError('Not a scalar protobuf type: $type');
  }
}

/// The default-value Dart expression for a singular scalar [type].
String _scalarDefault(String type) {
  switch (type) {
    case 'TYPE_FLOAT':
    case 'TYPE_DOUBLE':
      return '0.0';
    case 'TYPE_BOOL':
      return 'false';
    case 'TYPE_STRING':
      return "''";
    case 'TYPE_BYTES':
      return '<int>[]';
    default:
      return '0';
  }
}

/// Maps a message FQN to its flattened Dart class name.
String _messageDartType(String fqn) => _flattenName(fqn);

/// Maps an enum FQN to its flattened Dart class name.
String _enumDartType(String fqn) => _flattenName(fqn);

/// Flattens a (possibly cross-package, possibly nested) FQN to the Dart class
/// name used in the generated file. The package prefix is dropped and the
/// nested-type path is joined with `_` — matching [GenModelBuilder]'s naming.
String _flattenName(String fqn) {
  // Find the message/enum simple-name chain after the package. We can't know
  // the package boundary from the FQN alone, but the descriptor naming convention
  // joins all *type* segments (after the package) with `_`. The package segments
  // are lowercase-dotted; type segments start with an uppercase letter. Split on
  // '.' and keep the trailing run of segments whose first char is uppercase.
  final parts = fqn.split('.');
  final typeSegs = <String>[];
  for (var i = parts.length - 1; i >= 0; i--) {
    final p = parts[i];
    if (p.isNotEmpty &&
        p[0] == p[0].toUpperCase() &&
        p[0] != p[0].toLowerCase()) {
      typeSegs.insert(0, p);
    } else {
      break;
    }
  }
  if (typeSegs.isEmpty) return parts.last;
  return typeSegs.join('_');
}

/// PascalCase from a lowerCamel / snake identifier (for `whichX`, `hasX`).
String _pascal(String name) {
  if (name.isEmpty) return name;
  // If snake-cased, camelize first.
  final camel = name.contains('_') ? toLowerCamel(name) : name;
  return camel[0].toUpperCase() + camel.substring(1);
}

/// A FQN turned into a safe Dart identifier suffix (for builder fn names).
String _safe(String fqn) =>
    fqn.replaceAll('.', r'$').replaceAll(RegExp(r'[^A-Za-z0-9$_]'), '_');
