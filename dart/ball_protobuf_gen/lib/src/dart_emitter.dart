/// Dart target emitter: turns a [GenFile] into `.pb.dart` source.
///
/// The emitted shape follows §4.3 of `docs/PROTOBUF_CODEGEN_PLAN.md`: each
/// message is a **mutable class wrapping a `Map<String, Object?>` backing
/// store**, with typed getters/setters that read/write that map, plus an
/// **embedded `ball_protobuf` descriptor** carrying the resolved Editions
/// features. All serialization delegates to the runtime (`marshal`/`unmarshal`,
/// `messageToJson`/`messageFromJson`) — no wire logic is generated.
///
/// ## Descriptor graph
///
/// The bridge's resolved descriptors form a (possibly cyclic) object graph:
/// a message field's `'messageDescriptor'` is an identity reference to another
/// message's field list (so `recursive_message` / `corecursive` terminate). A
/// `const` Dart literal cannot express cycles, so instead of inlining the
/// referenced list we emit a single lazily-populated registry,
/// `_$descriptors` (`Map<String, List<Map<String, Object?>>>`), keyed by FQN.
/// Each field map is emitted as a literal **except** that `'messageDescriptor'`
/// (and a map field's message-typed `'messageDescriptor'`) becomes a registry
/// lookup `_lookup('<fqn>')`, breaking the cycle. Every class delegates to its
/// registry entry via `static List<Map<String, Object?>> get descriptor`.
library;

import 'gen_model.dart';

/// Header marking generated files (do not edit).
String _header(String protoPath) =>
    '// GENERATED CODE — DO NOT EDIT.\n'
    '//\n'
    '// Generated by protoc-gen-ball from $protoPath.\n'
    '// Mutable-over-Map models bound to the ball_protobuf runtime.\n'
    '//\n'
    '// ignore_for_file: prefer_const_constructors, non_constant_identifier_names\n'
    '// ignore_for_file: constant_identifier_names, library_prefixes\n'
    '// ignore_for_file: unnecessary_cast, unused_element, deprecated_member_use\n'
    '// ignore_for_file: lines_longer_than_80_chars, camel_case_types\n'
    '// ignore_for_file: annotate_overrides, prefer_final_fields\n';

/// Emits the full `.pb.dart` source for [file].
String emitDartFile(GenFile file) {
  final b = StringBuffer();
  b.write(_header(file.protoPath));
  b.writeln();
  b.writeln("import 'package:ball_protobuf/ball_protobuf.dart' as \$pb;");
  b.writeln();

  // Enums first (messages reference them).
  for (final e in file.enums) {
    _emitEnum(b, e);
    b.writeln();
  }

  // Messages (map-entry helpers are skipped — absorbed into map fields).
  for (final m in file.messages) {
    if (m.isMapEntry) continue;
    _emitMessage(b, m);
    b.writeln();
  }

  // The shared descriptor registry + lazy builders.
  _emitDescriptorRegistry(b, file);

  return b.toString();
}

// ---------------------------------------------------------------------------
// Enums
// ---------------------------------------------------------------------------

void _emitEnum(StringBuffer b, GenEnum e) {
  // Emit an int-backed enum: a class with const instances + a value map. An
  // int wrapper (not a Dart `enum`) so open enums can carry unknown integers
  // without crashing, mirroring the runtime's open-enum behavior.
  b.writeln('/// Generated from `${e.fullName}`.');
  b.writeln('class ${e.dartName} {');
  b.writeln('  final int value;');
  b.writeln('  final String name;');
  b.writeln('  const ${e.dartName}._(this.value, this.name);');
  b.writeln();

  // De-duplicate by number for the const instances (first declared name wins,
  // matching the registry's canonical-name choice); later aliases reuse it.
  final seen = <int>{};
  for (final v in e.values) {
    if (!seen.add(v.number)) continue;
    b.writeln(
      "  static const ${v.dartName} = "
      "${e.dartName}._(${v.number}, '${v.protoName}');",
    );
  }
  b.writeln();

  // All canonical instances, for `values` and lookup.
  final canonical = <GenEnumValue>[];
  final seen2 = <int>{};
  for (final v in e.values) {
    if (seen2.add(v.number)) canonical.add(v);
  }
  b.write('  static const List<${e.dartName}> values = [');
  b.write(canonical.map((v) => v.dartName).join(', '));
  b.writeln('];');
  b.writeln();

  // Construct from an integer; unknown values yield an unnamed instance so
  // open enums preserve out-of-range ints.
  b.writeln('  factory ${e.dartName}.fromValue(int value) {');
  b.writeln('    for (final v in values) {');
  b.writeln('      if (v.value == value) return v;');
  b.writeln('    }');
  b.writeln("    return ${e.dartName}._(value, 'UNKNOWN');");
  b.writeln('  }');
  b.writeln();
  b.writeln('  @override');
  b.writeln('  bool operator ==(Object other) =>');
  b.writeln('      other is ${e.dartName} && other.value == value;');
  b.writeln('  @override');
  b.writeln('  int get hashCode => value.hashCode;');
  b.writeln('  @override');
  b.writeln("  String toString() => '${e.dartName}.\$name';");
  b.writeln('}');
}

